  // Helper functions
  export const getCountries = () => {
    const countries = [...new Set(globalCities.map(city => city.country))];
    return countries.sort();
  };
  
  export const getCitiesByCountry = (country) => {
    return globalCities
      .filter(city => city.country === country)
      .map(city => city.name)
      .sort();
  };
  
  export const searchCities = (query) => {
    const lowerQuery = query.toLowerCase();
    return globalCities.filter(city => 
      city.name.toLowerCase().includes(lowerQuery) ||
      city.country.toLowerCase().includes(lowerQuery)
    );
  };
  
  // Helper: Get city count by country
  export const getCityCountByCountry = () => {
    const counts = {};
    globalCities.forEach(city => {
      counts[city.country] = (counts[city.country] || 0) + 1;
    });
    return counts;
  };
  
  // Helper: Validate if city exists in country
  export const isCityInCountry = (cityName, countryName) => {
    return globalCities.some(
      city => city.name === cityName && city.country === countryName
    );
  };